# 實驗主迴圈 (Experiment Main Loop)
# ==========================================
def run_experiments(mode="PROPOSED_MAS"):
    print(f"🚀 開始執行自動化實驗管線 (Starting Automated Experiment Pipeline) - 模式 (Mode): {mode}")
    print("=" * 60)
    
    with open(BUGS_DEF_PATH, "r", encoding="utf-8") as f:
        bugs = json.load(f)

    results = []

    for bug in bugs:
        print(f"\n🧪 測試案例 (Testing Case): {bug['bug_id']} - {bug['description']}")
        
        # 1. 備份與破壞 (Backup & Inject)
        if not backup_clean_code(bug["target_file"]):
            continue
        if not inject_fault(bug):
            restore_clean_code(bug) 
            continue

        # 2. 初始化 AI 狀態 (Initialize AI State)
        initial_state = {
            "messages": [HumanMessage(content=bug["user_prompt"])],
            "mode": mode,
            "next_node": "",
            "current_stage": "AnalyzeFailure",
            "iteration_count": 0,
            "max_repair_iterations": bug.get("max_repair_iterations", 5),
            "start_time": time.time(),
            "tool_error_count": 0,
            "llm_thinking_time": 0.0,
            "tool_exec_time": 0.0,
            "build_passed": False,
            "functional_passed": False,
            "expected_uart_regex": bug.get("expected_uart_regex", ""),
            "crash_patterns": bug.get("crash_patterns", [])
        }

        # 3. 啟動 LangGraph 代理人並計時 (Start LangGraph Agent and time it)
        start_time = time.time()
        final_node = ""
        error_occurred = False
        
        # [RQ4 指標追蹤變數] (RQ4 Metric Tracking Variables)
        total_tokens_consumed = 0
        tool_invocation_errors = 0
        total_tool_invocations = 0
        agent_steps = 0
        repair_iterations = 0
        final_stage = "AnalyzeFailure"
        llm_thinking_time = 0.0
        tool_exec_time = 0.0
        
        try:
            # 🟢 修改：大幅提升遞迴上限至 100，避免 Pass@k 實驗提早中斷
            # (Modified: Increase recursion limit to 100 to prevent early termination in Pass@k experiments)
            for output in mas_app.stream(initial_state, {"recursion_limit": 100}):
                for node_name, state_update in output.items():
                    print(f"\n🚀 [Agent Trajectory] 進入節點 (Entering Node): {node_name}")
                    final_node = node_name
                    agent_steps += 1
                    repair_iterations = max(repair_iterations, state_update.get("iteration_count", 0))
                    final_stage = state_update.get("current_stage", final_stage)
                    llm_thinking_time = max(llm_thinking_time, state_update.get("llm_thinking_time", 0.0))
                    tool_exec_time = max(tool_exec_time, state_update.get("tool_exec_time", 0.0))
                    
                    if "messages" in state_update and state_update["messages"]:
                        for msg in state_update["messages"]:
                            
                            # 指標 1: 結算 Token 消耗
                            if hasattr(msg, "usage_metadata") and msg.usage_metadata:
                                total_tokens_consumed += msg.usage_metadata.get("total_tokens", 0)
                                
                            # 指標 2: 偵測工具調用總數與錯誤率
                            if msg.type == "tool":
                                total_tool_invocations += 1 # 🟢 每次工具回傳都計入總數
                                if "❌" in msg.content or "Error:" in msg.content or "Exception" in msg.content:
                                    tool_invocation_errors += 1
                                    print(f"   ⚠️ [Tool Error Tracker] 捕捉到工具執行失敗！累計 (Errors/Total): {tool_invocation_errors}/{total_tool_invocations}")

                        last_msg = state_update["messages"][-1].content
                        print(f"💬 {node_name} Output:\n{last_msg[:200]}...\n") 
        except Exception as e:
            print(f"   ⚠️ 系統異常中斷 (System interrupted with error): {e}")
            error_occurred = True

        mttr = round(time.time() - start_time, 2)
        
        # 4. 驗證修復結果 (Verify Fix Results)
        is_fixed = check_fix_success(bug)
        functional_pass, runtime_reason = evaluate_runtime_signature(bug, is_fixed)
        
        if error_occurred:
            pass_at_k = "API_ERROR"
            functional_pass = "API_ERROR"
            print(f"🎯 修復狀態 (Fix Status): 失敗 (API 伺服器無回應 / API Server Unresponsive)")
        else:
            pass_at_k = 1 if is_fixed else 0
            print(f"🎯 修復狀態 (Fix Status): {'成功 (Success)' if is_fixed else '失敗 (Failed)'}")
            print(f"🧪 功能驗證 (Functional Validation): {'通過' if functional_pass else '未通過'} - {runtime_reason}")
        
        tool_error_rate = round(tool_invocation_errors / total_tool_invocations, 4) if total_tool_invocations > 0 else 0.0
        
        print(f"⏱️ 修復耗時 (MTTR): {mttr} 秒 (s)")
        print(f"🪙 Token 總消耗量 (Total Tokens): {total_tokens_consumed}")
        print(f"🛠️ 工具調用次數 (Tool Invocations): {total_tool_invocations}")
        print(f"🛠️ 工具錯誤率 (Tool Error Rate): {tool_error_rate:.2%} ({tool_invocation_errors}/{total_tool_invocations})")

        token_efficiency = total_tokens_consumed if pass_at_k == 1 else None
        overhead_ratio = round(llm_thinking_time / mttr, 4) if mttr > 0 else 0.0

        # 5. 記錄數據 (Record Data) - 將新指標寫入報表
        results.append({
            "Bug ID": bug["bug_id"],
            "Category": bug["category"],
            "Toolchain": bug.get("toolchain", ""),
            "Target": bug.get("target", ""),
            "LLM Mode": mode,
            "Human Baseline (s)": bug["human_baseline_time_sec"],
            "AI MTTR (s)": mttr,
            "Success (Pass@k)": pass_at_k,
            "k Bound": bug.get("max_repair_iterations", 5),
            "Repair Iterations": repair_iterations,
            "Functional Pass Rate": functional_pass,
            "Runtime Validation Reason": runtime_reason,
            "Expected UART Regex": bug.get("expected_uart_regex", ""),
            "Crash Patterns": ", ".join(bug.get("crash_patterns", [])),
            "Golden Patch": bug.get("golden_patch", ""),
            "Retrieval Expected Sources": ", ".join(bug.get("retrieval_expected_sources", [])),
            "Total Tokens": total_tokens_consumed,      
            "Token Efficiency": token_efficiency,
            "Total Tool Invocations": total_tool_invocations,
            "Tool Errors": tool_invocation_errors,            
            "Tool Invocation Error Rate": tool_error_rate,    
            "Agentic Interaction Overhead Ratio": overhead_ratio,
            "Agent Steps": agent_steps,               
            "Final Node": final_node,
            "Final Stage": final_stage
        })

        # 6. 還原環境準備下一輪 (Restore Environment for next round)
        restore_clean_code(bug)
        print("-" * 60)

    # 產出 CSV 報表 (Generate CSV Report)
    df = pd.DataFrame(results)
    csv_filename = os.path.join(RESULTS_DIR, f"experiment_results_{mode}.csv")
    df.to_csv(csv_filename, index=False, encoding="utf-8-sig")
    print(f"\n📊 實驗報表已儲存至 (Experiment report saved to): {csv_filename}")

if __name__ == "__main__":
    # Modes aligned with the revised thesis proposal:
    # B1 = Zero-Shot LLM
    # B2 = Single Agent + Text RAG
    # B3 = Closed-Loop Single Agent
    # PROPOSED_MAS = Multi-Agent Closed-Loop Framework
    run_experiments(mode="PROPOSED_MAS")
