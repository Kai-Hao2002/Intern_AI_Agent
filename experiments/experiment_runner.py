EXPERIMENTS_DIR = os.path.dirname(os.path.abspath(__file__))
BUGS_DEF_PATH = os.path.join(EXPERIMENTS_DIR, "fault_injection", "bugs_def.json")
BACKUP_DIR = os.path.join(EXPERIMENTS_DIR, "fault_injection", "backup_clean_code")
RESULTS_DIR = os.path.join(EXPERIMENTS_DIR, "results")
PROJECT_ROOT = os.path.abspath(os.path.join(EXPERIMENTS_DIR, ".."))

os.makedirs(BACKUP_DIR, exist_ok=True)
os.makedirs(RESULTS_DIR, exist_ok=True)

# ==========================================
# 核心功能函數 (Core Functions)
# ==========================================
def backup_clean_code(target_file_rel_path: str):
    """備份乾淨的原始碼 (Backup clean source code)"""
    abs_target = os.path.join(PROJECT_ROOT, target_file_rel_path)
    if not os.path.exists(abs_target):
        print(f"⚠️ 找不到目標檔案 (Target file not found): {abs_target}")
        return False
    
    backup_path = os.path.join(BACKUP_DIR, os.path.basename(target_file_rel_path))
    shutil.copy2(abs_target, backup_path)
    return True

def restore_clean_code(bug_def: dict):
    """
    還原乾淨的原始碼與環境 (Restore clean source code and environment)
    """
    target_file_rel_path = bug_def["target_file"]
    backup_path = os.path.join(BACKUP_DIR, os.path.basename(target_file_rel_path))
    abs_target = os.path.join(PROJECT_ROOT, target_file_rel_path)
    
    # 清除因為 rename_file 動作而產生的錯誤檔案 (Clean up the incorrect file generated by the rename_file action)
    if bug_def.get("fault_action") == "rename_file":
        fake_name = os.path.join(PROJECT_ROOT, bug_def.get("target_content"))
        if os.path.exists(fake_name):
            os.remove(fake_name)
    
    # 復原正確檔案 (Restore the correct file)
    if os.path.exists(backup_path):
        os.makedirs(os.path.dirname(abs_target), exist_ok=True)
        shutil.copy2(backup_path, abs_target)
        return True
    return False

def inject_fault(bug_def: dict):
    """
    根據定義注入多維度的錯誤 (Inject multi-dimensional faults based on definition)
    """
    abs_target = os.path.join(PROJECT_ROOT, bug_def["target_file"])
    action = bug_def.get("fault_action")
    
    if action == "replace_text":
        search = bug_def.get("fault_search")
        replace = bug_def.get("target_content")
        with open(abs_target, "r", encoding="utf-8") as f:
            code = f.read()
        if search in code:
            code = code.replace(search, replace)
            with open(abs_target, "w", encoding="utf-8") as f:
                f.write(code)
            print(f"🕷️ [Fault Injected] Replaced text in: {bug_def['target_file']}")
            return True
            
    elif action == "delete_file":
        if os.path.exists(abs_target):
            os.remove(abs_target)
            print(f"🕷️ [Fault Injected] Deleted file: {bug_def['target_file']}")
            return True
            
    elif action == "rename_file":
        new_name = os.path.join(PROJECT_ROOT, bug_def.get("target_content"))
        if os.path.exists(abs_target):
            os.rename(abs_target, new_name)
            print(f"🕷️ [Fault Injected] Renamed file to: {new_name}")
            return True
            
    elif action == "append_text":
        append_str = bug_def.get("target_content")
        with open(abs_target, "a", encoding="utf-8") as f:
            f.write(f"\n{append_str}\n")
        print(f"🕷️ [Fault Injected] Appended syntax error to: {bug_def['target_file']}")
        return True
            
    print(f"❌ 錯誤注入失敗 (Fault injection failed for): {bug_def['bug_id']}")
    return False

def check_fix_success(bug_def: dict):
    """
    動態驗證 AI 是否成功修復各種狀態 (Dynamically verify if AI successfully fixed various states)
    """
    abs_target = os.path.join(PROJECT_ROOT, bug_def["target_file"])
    action = bug_def.get("fault_action")
    
    # 只要是修復，目標檔案最終都必須存在 (For any fix, the target file must ultimately exist)
    if not os.path.exists(abs_target):
        return False
        
    if action == "replace_text":
        with open(abs_target, "r", encoding="utf-8") as f:
            current_code = f.read()
        return bug_def["fault_search"] in current_code and bug_def["target_content"] not in current_code
        
    elif action == "delete_file" or action == "rename_file":
        # 只要檔案被 AI 透過 bash 建立或重新命名回來即算成功
        # (Success is achieved as long as the file is created or renamed back by AI via bash)
        return True 
        
    elif action == "append_text":
        with open(abs_target, "r", encoding="utf-8") as f:
            current_code = f.read()
        # 附加的惡意污染文本被移除即算成功 (Success is achieved if the appended malicious text is removed)
        return bug_def["target_content"] not in current_code
        
    return False

# ==========================================
# 實驗主迴圈 (Experiment Main Loop)
# ==========================================
def run_experiments(mode="PROPOSED_MAS"):
    print(f"🚀 開始執行自動化實驗管線 (Starting Automated Experiment Pipeline) - 模式 (Mode): {mode}")
    print("=" * 60)
    
    with open(BUGS_DEF_PATH, "r", encoding="utf-8") as f:
        bugs = json.load(f)

    results = []

    for bug in bugs:
        print(f"\n🧪 測試案例 (Testing Case): {bug['bug_id']} - {bug['description']}")
        
        # 1. 備份與破壞 (Backup & Inject)
        if not backup_clean_code(bug["target_file"]):
            continue
        if not inject_fault(bug):
            restore_clean_code(bug) 
            continue

        # 2. 初始化 AI 狀態 (Initialize AI State)
        initial_state = {
            "messages": [HumanMessage(content=bug["user_prompt"])],
            "mode": mode,
            "iteration_count": 0, 
            "retry_count": 0
        }

        # 3. 啟動 LangGraph 代理人並計時 (Start LangGraph Agent and time it)
        start_time = time.time()
        final_node = ""
        error_occurred = False
        
        # [RQ4 指標追蹤變數] (RQ4 Metric Tracking Variables)
        total_tokens_consumed = 0
        tool_invocation_errors = 0
        agent_steps = 0
        
        try:
            # 限制遞迴深度避免無限死結
            for output in mas_app.stream(initial_state, {"recursion_limit": 15}):
                for node_name, state_update in output.items():
                    print(f"\n🚀 [Agent Trajectory] 進入節點 (Entering Node): {node_name}")
                    final_node = node_name
                    agent_steps += 1
                    
                    if "messages" in state_update and state_update["messages"]:
                        #  動態解析此節點產生的所有新訊息 (Dynamically parse all new messages generated by this node)
                        for msg in state_update["messages"]:
                            
                            # 指標 1: 結算 Token 消耗 (Metric 1: Tally Token Consumption)
                            if hasattr(msg, "usage_metadata") and msg.usage_metadata:
                                total_tokens_consumed += msg.usage_metadata.get("total_tokens", 0)
                                
                            # 指標 2: 偵測工具調用錯誤 (Metric 2: Detect Tool Invocation Errors)
                            if msg.type == "tool":
                                if "❌" in msg.content or "Error:" in msg.content or "Exception" in msg.content:
                                    tool_invocation_errors += 1
                                    print(f"   ⚠️ [Tool Error Tracker] 捕捉到工具執行失敗 (Tool failure caught)！累計 (Total): {tool_invocation_errors}")

                        last_msg = state_update["messages"][-1].content
                        print(f"💬 {node_name} Output:\n{last_msg[:200]}...\n") 
        except Exception as e:
            print(f"   ⚠️ 系統異常中斷 (System interrupted with error): {e}")
            error_occurred = True

        mttr = round(time.time() - start_time, 2)
        
        # 4. 驗證修復結果 (Verify Fix Results)
        is_fixed = check_fix_success(bug)
        
        if error_occurred:
            pass_at_k = "API_ERROR"
            print(f"🎯 修復狀態 (Fix Status): 失敗 (API 伺服器無回應 / API Server Unresponsive)")
        else:
            pass_at_k = 1 if is_fixed else 0
            print(f"🎯 修復狀態 (Fix Status): {'成功 (Success)' if is_fixed else '失敗 (Failed)'}")
        
        print(f"⏱️ 修復耗時 (MTTR): {mttr} 秒 (s)")
        print(f"🪙 Token 總消耗量 (Total Tokens): {total_tokens_consumed}")
        print(f"🛠️ 工具錯誤次數 (Tool Errors): {tool_invocation_errors}")

        # 5. 記錄數據 (Record Data) - 將新指標寫入報表
        results.append({
            "Bug ID": bug["bug_id"],
            "Category": bug["category"],
            "LLM Mode": mode,
            "Human Baseline (s)": bug["human_baseline_time_sec"],
            "AI MTTR (s)": mttr,
            "Success (Pass@k)": pass_at_k,
            "Total Tokens": total_tokens_consumed,      
            "Tool Errors": tool_invocation_errors,      
            "Agent Steps": agent_steps,               
            "Final Node": final_node
        })

        # 6. 還原環境準備下一輪 (Restore Environment for next round)
        restore_clean_code(bug)
        print("-" * 60)

    # 產出 CSV 報表 (Generate CSV Report)
    df = pd.DataFrame(results)
    csv_filename = os.path.join(RESULTS_DIR, f"experiment_results_{mode}.csv")
    df.to_csv(csv_filename, index=False, encoding="utf-8-sig")
    print(f"\n📊 實驗報表已儲存至 (Experiment report saved to): {csv_filename}")

if __name__ == "__main__":
    # 可以在此處替換為 B1, B2, B3 來執行消融實驗對照組
    # You can replace this with B1, B2, B3 to run ablation study baselines
    run_experiments(mode="PROPOSED_MAS")